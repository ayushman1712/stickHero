package com.example.stickherofx;

class Point {
    double x;
    double y;

    public Point(double v) {
    }

    public double getX() {
        return x;
    }

    public Point() {
    }

    public void setX(double x) {
        this.x = x;
    }

    public double getY() {
        return y;
    }

    public Point(double x, double y) {
        this.x = x;
        this.y = y;
    }

    public void setY(double y) {
        this.y = y;
    }
}
